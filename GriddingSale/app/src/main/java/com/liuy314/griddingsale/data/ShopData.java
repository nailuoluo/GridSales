public class ShopData {

    private String mShopName;
    private String mShopCode;
    private String mShopLatitude;
    private String mShopLongitude;

    public String getmShopCode() {
        return mShopCode;
    }

    public void setmShopCode(String mShopCode) {
        this.mShopCode = mShopCode;
    }

    public String getmShopLatitude() {
        return mShopLatitude;
    }

    public void setmShopLatitude(String mShopLatitude) {
        this.mShopLatitude = mShopLatitude;
    }

    public String getmShopLongitude() {
        return mShopLongitude;
    }

    public void setmShopLongitude(String mShopLongitude) {
        this.mShopLongitude = mShopLongitude;
    }

    public String getmShopName() {
        return mShopName;
    }

    public void setmShopName(String mShopName) {
        this.mShopName = mShopName;
    }
}
